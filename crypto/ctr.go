package crypto

import "crypto/cipher"

// ***********************************************************************************************
// * SUMMARY:
// * WARNING:
// * HISTORY:
// *    -create: 2021/12/16 16:45:02 ColeCai.
// ***********************************************************************************************
func CTRCrypto(block cipher.Block, src, iv []byte) ([]byte, error) {
	dst := make([]byte, len(src))
	ctr := cipher.NewCTR(block, iv)
	ctr.XORKeyStream(dst, src)
	return dst, nil
}
